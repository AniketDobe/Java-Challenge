public class operators{
    public static void main(String args[]){
        int a = 6;
        int b = 5;
        // Arithmetic operators +, -, /, *, %
        System.out.println(a+b);

        // Assignment operators =, +=, -=, /=, *=, %=
        System.out.println(a+=b);

        // Comparision operators >, <, ==, , <=, >=
        System.out.println(a>b);

        // Logical operators &&, ||, !
        System.out.println(a>b && b>1);
        
    }
}